type SlackBlock =
	| { type: 'header'; text: { type: 'plain_text'; text: string } }
	| { type: 'section'; text: { type: 'mrkdwn'; text: string } }
	| { type: 'divider' }
	| { type: 'context'; elements: { type: 'mrkdwn'; text: string }[] };

/**
 * Generate a Slack Block Kit payload for sharing a Traek conversation.
 * Returns a JSON object you can POST to Slack's `chat.postMessage` API.
 */
export function snapshotToSlack(
	snapshot: ConversationSnapshot,
	options: { shareUrl?: string; maxMessages?: number } = {}
): { blocks: SlackBlock[] } {
	const { shareUrl, maxMessages = 6 } = options;
	const title = snapshot.title ?? 'Untitled Conversation';
	const nodeMap = buildNodeMap(snapshot.nodes);
	const roots = getRoots(snapshot);
	const threads = extractThreads(roots, nodeMap);

	// Pick the first (main) thread for the preview
	const mainThread = threads[0] ?? [];
	const preview = mainThread.slice(0, maxMessages);

	const blocks: SlackBlock[] = [
		{
			type: 'header',
			text: { type: 'plain_text', text: `💬 ${title}` }
		},
		{
			type: 'context',
			elements: [
				{
					type: 'mrkdwn',
					text: `*Traek Conversation* · ${snapshot.nodes.length} messages · ${threads.length} branch${threads.length !== 1 ? 'es' : ''}`
				}
			]
		},
		{ type: 'divider' }
	];

	for (const node of preview) {
		const emoji = node.role === 'user' ? '👤' : node.role === 'assistant' ? '🤖' : '⚙️';
		const label = roleLabel(node.role);
		const snippet = node.content.trim().slice(0, 280);
		const truncated = node.content.trim().length > 280 ? '…' : '';

		blocks.push({
			type: 'section',
			text: {
				type: 'mrkdwn',
				text: `${emoji} *${label}*\n>${snippet}${truncated}`
			}
		});
	}

	if (mainThread.length > maxMessages) {
		blocks.push({
			type: 'context',
			elements: [
				{
					type: 'mrkdwn',
					text: `_…and ${mainThread.length - maxMessages} more messages_`
				}
			]
		});
	}

	if (shareUrl) {
		blocks.push({ type: 'divider' });
		blocks.push({
			type: 'section',
			text: {
				type: 'mrkdwn',
				text: `<${shareUrl}|Open in Traek →>`
			}
		});
	}

	return { blocks };
}

/**
 * Serialize a Slack Block Kit payload to a JSON string.
 */
export function snapshotToSlackJSON(
	snapshot: ConversationSnapshot,
	options?: { shareUrl?: string; maxMessages?: number }
): string {
	return JSON.stringify(snapshotToSlack(snapshot, options), null, 2);
}

// ---------------------------------------------------------------------------
// Discord embed format
// ---------------------------------------------------------------------------

interface DiscordEmbed {
	title: string;
	description: string;
	color: number;
	fields: { name: string; value: string; inline?: boolean }[];
	footer: { text: string };
	url?: string;
}

interface DiscordPayload {
	content: string;
	embeds: DiscordEmbed[];
}

/**
 * Generate a Discord webhook payload for sharing a Traek conversation.
 * Returns a JSON object you can POST to a Discord webhook URL.
 */
export function snapshotToDiscord(
	snapshot: ConversationSnapshot,
	options: { shareUrl?: string; maxMessages?: number } = {}
): DiscordPayload {
	const { shareUrl, maxMessages = 4 } = options;
	const title = snapshot.title ?? 'Untitled Conversation';
	const nodeMap = buildNodeMap(snapshot.nodes);
	const roots = getRoots(snapshot);
	const threads = extractThreads(roots, nodeMap);
	const mainThread = threads[0] ?? [];
	const preview = mainThread.slice(0, maxMessages);

	const fields: { name: string; value: string; inline?: boolean }[] = preview.map((node) => {
		const label = roleLabel(node.role);
		const snippet = node.content.trim().slice(0, 200);
		const truncated = node.content.trim().length > 200 ? '…' : '';
		return {
			name: label,
			value: snippet + truncated || '*(empty)*'
		};
	});

	if (mainThread.length > maxMessages) {
		fields.push({
			name: '…',
			value: `*${mainThread.length - maxMessages} more messages*`,
			inline: true
		});
	}

	const embed: DiscordEmbed = {
		title: `💬 ${title}`,
		description: `**${snapshot.nodes.length}** messages across **${threads.length}** branch${threads.length !== 1 ? 'es' : ''}`,
		color: 0x00d8ff, // Traek cyan
		fields,
		footer: { text: 'Traek · Spatial AI Conversations · traek.dev' },
		...(shareUrl ? { url: shareUrl } : {})
	};

	return {
		content: '',
		embeds: [embed]
	};
}

/**
 * Serialize a Discord payload to a JSON string.
 */
export function snapshotToDiscordJSON(
	snapshot: ConversationSnapshot,
	options?: { shareUrl?: string; maxMessages?: number }
): string {
	return JSON.stringify(snapshotToDiscord(snapshot, options), null, 2);
}

// ---------------------------------------------------------------------------
// Utilities
// ---------------------------------------------------------------------------

function escapeHtml(str: string): string {
	return str
		.replace(/&/g, '&amp;')
		.replace(/</g, '&lt;')
		.replace(/>/g, '&gt;')
		.replace(/"/g, '&quot;')
		.replace(/'/g, '&#039;');
}

/**
 * Count the number of branch points in a conversation tree.
 * A branch point is any node that has more than one child.
 */
export function countBranchPoints(snapshot: ConversationSnapshot): number {
	const childCounts = new Map<string, number>();

	for (const node of snapshot.nodes) {
		for (const parentId of node.parentIds) {
			childCounts.set(parentId, (childCounts.get(parentId) ?? 0) + 1);
		}
	}

	let count = 0;
	for (const c of childCounts.values()) {
		if (c > 1) count++;
	}
	return count;
}

/**
 * Get a short preview text from the first N messages of a conversation.
 */
export function getConversationPreview(snapshot: ConversationSnapshot, maxChars = 200): string {
	const mainThread = snapshot.nodes.filter((n) => n.parentIds.length === 0);
	if (mainThread.length === 0) return '';

	const firstNode = mainThread[0];
	return firstNode.content.trim().slice(0, maxChars);
}
