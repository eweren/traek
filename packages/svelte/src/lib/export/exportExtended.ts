/**
 * Extended export utilities for Traek conversations.
 *
 * Covers: print/PDF layout, Slack Block Kit, Discord embeds, and a
 * helper for programmatic print windows.
 */

import type { ConversationSnapshot, SerializedNode } from '../persistence/schemas';

// ---------------------------------------------------------------------------
// Internal helpers (shared with exportUtils.ts logic)
// ---------------------------------------------------------------------------

function buildNodeMap(nodes: SerializedNode[]): Map<string, SerializedNode> {
	const map = new Map<string, SerializedNode>();
	for (const node of nodes) map.set(node.id, node);
	return map;
}

function extractThreads(
	roots: SerializedNode[],
	nodeMap: Map<string, SerializedNode>
): SerializedNode[][] {
	const threads: SerializedNode[][] = [];

	function dfs(node: SerializedNode, path: SerializedNode[]): void {
		const newPath = [...path, node];
		const children = Array.from(nodeMap.values()).filter((n) => n.parentIds.includes(node.id));
		if (children.length === 0) {
			threads.push(newPath);
		} else {
			for (const child of children) dfs(child, newPath);
		}
	}

	for (const root of roots) dfs(root, []);
	return threads;
}

function getRoots(snapshot: ConversationSnapshot): SerializedNode[] {
	return snapshot.nodes.filter((n) => n.parentIds.length === 0);
}

function roleLabel(role: string): string {
	if (role === 'user') return 'User';
	if (role === 'assistant') return 'Assistant';
	return 'System';
}

