// PDF export: HTML string with page-break hints
// ---------------------------------------------------------------------------

/**
 * Generate a self-contained HTML document optimized for PDF export.
 * Inserts page-break hints at branch points (where the tree diverges).
 *
 * The returned string can be opened in a browser and printed to PDF.
 */
export function snapshotToPDFHtml(snapshot: ConversationSnapshot): string {
	const title = snapshot.title ?? 'Untitled Conversation';
	const createdAt = new Date(snapshot.createdAt).toLocaleString();
	const nodeMap = buildNodeMap(snapshot.nodes);
	const roots = getRoots(snapshot);
	const threads = extractThreads(roots, nodeMap);

	const threadHtml = threads
		.map((thread, idx) => {
			const isBranchPoint = idx > 0;
			const branchStyle = isBranchPoint ? 'page-break-before: always;' : '';
			const messages = thread
				.map((node) => {
					const label = roleLabel(node.role);
					const cls = `role-${node.role}`;
					const content = node.content.trim() || '(No content)';
					return `
				<div class="message ${cls}">
					<div class="role-label">${label}</div>
					<div class="content">${escapeHtml(content)}</div>
				</div>`;
				})
				.join('\n');

			return `
		<div class="thread" style="${branchStyle}">
			<div class="thread-label">Branch ${idx + 1}</div>
			${messages}
		</div>`;
		})
		.join('\n<hr class="branch-divider" />\n');

	return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8" />
<meta name="viewport" content="width=device-width, initial-scale=1.0" />
<title>${escapeHtml(title)}</title>
<style>
* { box-sizing: border-box; margin: 0; padding: 0; }
body {
	font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif;
	font-size: 13px;
	line-height: 1.7;
	color: #111;
	background: #fff;
	padding: 32px;
	max-width: 720px;
	margin: 0 auto;
}
h1 { font-size: 22px; font-weight: 700; margin-bottom: 6px; }
.meta { font-size: 11px; color: #777; margin-bottom: 32px; }
.thread { margin-bottom: 40px; }
.thread-label {
	font-size: 10px;
	font-weight: 700;
	text-transform: uppercase;
	letter-spacing: 0.1em;
	color: #aaa;
	margin-bottom: 14px;
}
.message {
	border-left: 3px solid #e0e0e0;
	padding: 10px 14px;
	margin-bottom: 14px;
	page-break-inside: avoid;
	break-inside: avoid;
}
.message.role-user { border-left-color: #0077cc; }
.message.role-assistant { border-left-color: #c04000; }
.role-label {
	font-size: 10px;
	font-weight: 700;
	text-transform: uppercase;
	letter-spacing: 0.05em;
	margin-bottom: 6px;
	color: #888;
}
.message.role-user .role-label { color: #0077cc; }
.message.role-assistant .role-label { color: #c04000; }
.content { white-space: pre-wrap; font-size: 13px; }
.branch-divider {
	border: none;
	border-top: 1px dashed #ddd;
	margin: 28px 0;
}
footer {
	margin-top: 48px;
	border-top: 1px solid #eee;
	padding-top: 14px;
	font-size: 10px;
	color: #bbb;
	text-align: center;
}
@page { margin: 18mm 22mm; }
@media print {
	.thread[style*="page-break-before"] { page-break-before: always; }
}
</style>
</head>
<body>
<h1>${escapeHtml(title)}</h1>
<div class="meta">Exported from Traek · ${escapeHtml(createdAt)} · ${snapshot.nodes.length} messages</div>
${threadHtml}
<footer>Generated by Traek · traek.dev</footer>
</body>
</html>`;
}

// ---------------------------------------------------------------------------
// Print window helper
// ---------------------------------------------------------------------------

/**
 * Open a print-optimised popup window for the given snapshot and trigger
 * the browser's native print dialog. Works in browser environments only.
 */
export function printConversation(snapshot: ConversationSnapshot): void {
	if (typeof window === 'undefined') return;

	const html = snapshotToPDFHtml(snapshot);
	const win = window.open('', '_blank', 'width=800,height=900');
	if (!win) return;

	win.document.open();
	win.document.write(html);
	win.document.close();

	win.addEventListener('load', () => {
		win.print();
	});
}

